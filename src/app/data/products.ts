export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id);
}

export function getProductsByCategory(category: string): Product[] {
  if (category === 'sport-collection') {
    return products.filter(p => ['shoes', 'men', 'women'].includes(p.category));
  }
  if (category === 'ball-collection') {
    return products.filter(p => p.category === 'balls');
  }
  return products.filter(p => p.category === category);
}

export function getFeaturedProducts(limit: number = 4): Product[] {
  return products.slice(0, limit);
}

export function getBestSellers(limit: number = 4): Product[] {
  return products
    .filter(p => p.badge === 'Best Seller')
    .slice(0, limit);
}
